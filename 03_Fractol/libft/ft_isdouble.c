#include "libft.h"

int	ft_isdouble(char *s)
{
	if (!*s)
		return (0);
	if (*s == '-')
		if (!(*(++s) >= '0' && *s <= '9'))
			return (0);
	while (*s && *s != '.')
		if (!(*s++ >= '0' && *s <= '9'))
			return (0);
	if (*s == '.')
	{
		if (!(*(++s) >= '0' && *s <= '9'))
			return (0);
		while (*s)
			if (!(*s++ >= '0' && *s <= '9'))
				return (0);
	}
	return (1);
}
